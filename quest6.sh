
Генерация ключей:
src/ai_help/keygen.sh

Список всего в папке ai_help:
migasdel@ga-l1 ai_help % ls

Зайти в папку:
ai_help % cd key

Удалить все файлы такого вида “file…”:
key % rm file*

Выйти обратно в ai_help:
key % cd ..

Дать доступ файлу:
ai_help % chmod +x unifier.sh

Запустить его:
ai_help % ./unifier.sh 
